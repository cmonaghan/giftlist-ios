-(NSManagedObjectContext *) managedObjectContext {
    if(!_managedObjectContext) {
        _managedObjectContext = [(GLAppDelegate *)[[UIApplication sharedApplication] delegate] managedObjectContext];
    }
    return _managedObjectContext;
}

- (NSFetchedResultsController *)fetchedResultsController {
    
    if (_fetchedResultsController != nil) {
        return _fetchedResultsController;
    }
    
    NSFetchRequest *fetchRequest = [[NSFetchRequest alloc] init];
    NSEntityDescription *entity = [NSEntityDescription
                                   entityForName:@"GiftListItem" inManagedObjectContext:self.managedObjectContext];
    [fetchRequest setEntity:entity];
    
    NSSortDescriptor *sort = [[NSSortDescriptor alloc]
                              initWithKey:@"itemId" ascending:YES];
    [fetchRequest setSortDescriptors:[NSArray arrayWithObject:sort]];
    
    [fetchRequest setFetchBatchSize:20];
    
    NSFetchedResultsController *theFetchedResultsController =
    [[NSFetchedResultsController alloc] initWithFetchRequest:fetchRequest
                                        managedObjectContext:self.managedObjectContext sectionNameKeyPath:nil
                                                   cacheName:@"Root"];
    _fetchedResultsController = theFetchedResultsController;
    _fetchedResultsController.delegate = self;
    
    return _fetchedResultsController;
    
}

- (void)configureCell:(UITableViewCell *)rawCell atIndexPath:(NSIndexPath *)indexPath {
    GiftListItem *info = [_fetchedResultsController objectAtIndexPath:indexPath];
    if([rawCell isKindOfClass:[GLTableViewCell class]]) {
        GLTableViewCell *cell = (GLTableViewCell *) rawCell;
        cell.title.text = info.title;
        cell.price.text = [NSString stringWithFormat:@"$%@", info.price];
        
        NSURL *url = [NSURL URLWithString: info.imageUrl];
        NSData *data = [NSData dataWithContentsOfURL:url];
        UIImage *image = [[UIImage alloc] initWithData:data];
        
        cell.image.image = image;
    }
}

#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    id  sectionInfo = [[_fetchedResultsController sections] objectAtIndex:section];
    return [sectionInfo numberOfObjects];
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *cellIdentifier = @"GLCell";
    UITableViewCell *rawCell = [tableView dequeueReusableCellWithIdentifier:cellIdentifier];
    
    [self configureCell:rawCell atIndexPath:indexPath];
    
    return rawCell;
}

